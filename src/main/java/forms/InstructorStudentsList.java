    public final void fillTable(ResultSet result){
        try{
            while(result.next()){
            
                String studentName = result.getString(1);
                String courseTitle = result.getString(2);
                String grade = result.getString(3);
                
                
                Object[] content = {studentName, courseTitle, grade};
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.addRow(content);
            }
            } catch (SQLException ex) {
                System.out.println("Error in fillTable method of InstructorStudentsList class: "+ex.getMessage());
            }
    }

